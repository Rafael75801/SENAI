print('Vou descobrir que número você está pensando.')
print('Pense um número de 1 a 10 (obrigatorio).')
num = int(input('Qual seu número (prometo que não vou falar): '))
print('Vamos multiplicar seu número por 2.')
mult = num * 2
print('Agora vamos multiplicar por 5')
mult2 = mult * 5
print('Agora vamos dividir o resultado dessa última multiplicação pelo seu número.')
div = mult2 / num
print('Agora vamos subtrair ele por 7')
sub = div - 7
print(f'Seu número é... {sub}')

